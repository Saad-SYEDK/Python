# Operators in Python
# We have different types of operators like arithmetic operators, assignment operators, comparison operators, logical operators, and more.



# Conditional operators
# Conditional operators are used to perform comparisons between values.
# The most common conditional operators in Python are:
# - Equal to (==)
# - Not equal to (!=)
# - Greater than (>)
# - Less than (<)
# - Greater than or equal to (>=)
# - Less than or equal to (<=)

# Here's an example of how to use conditional operators in Python:
a = 10
b = 5

if a > b:
    print("a is greater than b")
elif a < b:
    print("a is less than b")
else:
    print("a is equal to b")




# Arithmetic operators
# Arithmetic operators are used to perform mathematical operations on numbers.
# The most common arithmetic operators in Python are:
# - Addition (+)
# - Subtraction (-)
# - Multiplication (*)
# - Division (/)
# - Modulus (%)
# - Exponentiation (**)
# - Floor division (//)
# Here's an example of how to use arithmetic operators in Python:
a = 10
b = 3
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)
print("Floor division:", a // b)




# Logical operators
# Logical operators are used to combine multiple conditions.
# The most common logical operators in Python are:
# - and
# - or
# - not
# Here's an example of how to use logical operators in Python:
a = 10
b = 3
if a > b and a != 0:
    print("a is greater than b and a is not zero")
if a < b or b == 3:
    print("a is less than b or b is equal to 3")
if not a == b:
    print("a is not equal to b")




# Assignment operators
# Assignment operators are used to assign values to variables.
# The most common assignment operators in Python are:
# - =
# - +=
# - -=
# - *=
# - /=
# - %=
# - **=
# - //=
# Here's an example of how to use assignment operators in Python:
a = 10
b = 5
a += b
print("Addition Assignment:", a)
a -= b
print("Subtraction Assignment:", a)
a *= b
print("Multiplication Assignment:", a)
a /= b
print("Division Assignment:", a)
a %= b
print("Modulus Assignment:", a)
a **= b
print("Exponentiation Assignment:", a)
a //= b
print("Floor Division Assignment:", a)


# These are some of the most common operators in Python. There are many more operators available for different purposes, such as bitwise operators, membership operators, and identity operators.
