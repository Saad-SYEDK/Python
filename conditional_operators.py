# Conditional operators are used to perform comparisons between values.
# The most common conditional operators in Python are:
# - Equal to (==)
# - Not equal to (!=)
# - Greater than (>)
# - Less than (<)
# - Greater than or equal to (>=)
# - Less than or equal to (<=)

# Here's an example of how to use conditional operators in Python:

a = 10
b = 5

if a > b:
    print("a is greater than b")
elif a < b:
    print("a is less than b")
else:
    print("a is equal to b")
